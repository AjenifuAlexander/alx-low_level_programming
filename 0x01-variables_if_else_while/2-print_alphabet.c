#include <stdio.h>
#include <ctype.h>

/**
 * main - Entry point
 *
 * Description: 'A program that prints the alphabet in lowercase.'
 *
 * Return: Always 0 (Success)
 *
*/
	int main(void)
{
		char i;

		int lowerCase = 'a';

		while (lowerCase <= 'z')
		{
			putchar(lowerCase <= 'z');

			lowerCase += 1;
		}

		putchar (i);
	putchar ("\n");
	Return(0);

}
